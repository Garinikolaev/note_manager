status = input("Введите текущий статус заметки: ") # Запрашиваем у пользователя статус заметки
print("Текущий статус заметки: ", status) # Отображаем статус заметки
def change_status(): # Предлагаем пользователю изменить статус заметки
    print("Выберите новый статус заметки:") # список возможных статусов перед вводом
    print("1. выполнено")
    print("2. в процессе")
    print("3. отложено")
    try:
        choice = int(input("Ваш выбор: ")) # Обновляет статус на основе ввода пользователя
        if choice in [1, 2, 3]:
            return choice
        else:
            print("Неправильный выбор. Попробуйте еще раз.")
            return change_status()
    except ValueError:
        print("Ошибка ввода. Введите целое число.") # Проверяет корректность введённого значения
        return change_status()
choice = change_status() # Программа обновляет статус
print("Статус заметки успешно обновлён на: ", ['выполнено', 'в процессе', 'отложено'][choice - 1])